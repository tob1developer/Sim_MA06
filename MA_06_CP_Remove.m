function [CP_Remve] = MA_06_CP_Remove(N,V)

% CP_Remove------------

CP_Remve    = zeros(N,N+V);
O_V         = zeros(N,V);
I_N         = eye(N);
for i =1:N
    CP_Remve(i,:) = [O_V(i,:) I_N(i,:)];
end