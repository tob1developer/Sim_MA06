function [CP_insert] = MA_06_CP_insert(N,V)

% CP_insert------------
O_I      = zeros(V,N);
O_V_N_V  = zeros(V,N-V);
I_V      = eye(V);
for i=1:V
   O_I(i,:) = [O_V_N_V(i,:) I_V(i,:)] ;
end
I_N      = eye(N);
CP_insert = [O_I;I_N];