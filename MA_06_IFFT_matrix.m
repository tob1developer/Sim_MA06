function [W_H] = MA_06_IFFT_matrix(N)
% N=4
W_H = zeros(N);
for i =1:N
    for m= 1:N
        W_H(i,m) = exp(j*2*pi/N*(i-1)*(m-1));
    end
end
W_H = 1/sqrt(N)*W_H;